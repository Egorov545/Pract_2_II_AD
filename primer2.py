import pandas as pd

# Создаем простую таблицу продаж
data = {
    'Дата': ['2024-04-01', '2024-04-01', '2024-04-02', '2024-04-03', '2024-04-03'],
    'Товар': ['Ткань хлопок', 'Нитки', 'Ткань шелк', 'Ткань хлопок', 'Пуговицы'],
    'Количество': [5, 10, 2, 3, 20],
    'Цена': [200, 50, 800, 200, 10]
}

df = pd.DataFrame(data)

# Добавляем выручку (Количество * Цена)
df['Выручка'] = df['Количество'] * df['Цена']

print("Таблица продаж:")
print(df)
print("\n" + "-"*40 + "\n")

# 1. Общие показатели
print("Общие показатели:")
print(f"Общая выручка: {df['Выручка'].sum()} руб.")
print(f"Всего товаров продано: {df['Количество'].sum()} шт.")

# 2. Самый продаваемый товар (по количеству)
top_product = df.groupby('Товар')['Количество'].sum().idxmax()
print(f"Самый продаваемый товар: {top_product}")

# 3. Выручка по дням
df['Дата'] = pd.to_datetime(df['Дата'])
revenue_by_day = df.groupby(df['Дата'].dt.day_name())['Выручка'].sum()
print(f"Выручка по дням недели:")
print(revenue_by_day)

# 4. Средний чек (средняя выручка с одной позиции)
print(f"Средний чек: {df['Выручка'].mean():.0f} руб.")